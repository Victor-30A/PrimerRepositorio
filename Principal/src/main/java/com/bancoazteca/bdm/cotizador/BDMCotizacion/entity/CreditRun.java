package com.bancoazteca.bdm.cotizador.BDMCotizacion.entity;

public class CreditRun {
	private String canal;
	private String pais;
	private String periodo;
	private String min;
	private String max;
	private String tasa;
	private String sucursal;
	private String nivelCR;
	private String descuento;
	private String capacidadDePago;
	private String plazoIni;
	private String plazoFin;
	public String getCanal() {
		return canal;
	}
	public void setCanal(String canal) {
		this.canal = canal;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getPeriodo() {
		return periodo;
	}
	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}
	public String getMin() {
		return min;
	}
	public void setMin(String min) {
		this.min = min;
	}
	public String getMax() {
		return max;
	}
	public void setMax(String max) {
		this.max = max;
	}
	public String getTasa() {
		return tasa;
	}
	public void setTasa(String tasa) {
		this.tasa = tasa;
	}
	public String getSucursal() {
		return sucursal;
	}
	public void setSucursal(String sucursal) {
		this.sucursal = sucursal;
	}
	public String getNivelCR() {
		return nivelCR;
	}
	public void setNivelCR(String nivelCR) {
		this.nivelCR = nivelCR;
	}
	public String getDescuento() {
		return descuento;
	}
	public void setDescuento(String descuento) {
		this.descuento = descuento;
	}
	public String getCapacidadDePago() {
		return capacidadDePago;
	}
	public void setCapacidadDePago(String capacidadDePago) {
		this.capacidadDePago = capacidadDePago;
	}
	public String getPlazoIni() {
		return plazoIni;
	}
	public void setPlazoIni(String plazoIni) {
		this.plazoIni = plazoIni;
	}
	public String getPlazoFin() {
		return plazoFin;
	}
	public void setPlazoFin(String plazoFin) {
		this.plazoFin = plazoFin;
	}

}
